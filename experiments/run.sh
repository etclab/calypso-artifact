        Example: $0 clean

    stop
        Stop all running services
        Example: $0 stop

    list
        Show recent benchmark results
        Example: $0 list

APPROACHES:
    plain, jwt, enclave, enclave-jwt, wkdibe, calypso
    plain-udp, jwt-udp, enclave-udp, enclave-jwt-udp, wkdibe-udp, calypso-udp

DEFAULTS:
    queries=$DEFAULT_QUERIES, services=$DEFAULT_SERVICES, namespaces=$DEFAULT_NAMESPACES
EOF
            ;;
        *)
            die "Unknown command: $cmd\nRun '$0 help' for usage"
            ;;
    esac
}

main "$@"