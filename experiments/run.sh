#!/usr/bin/env bash
set -e

# Calypso DNS Evaluation Runner
# Usage:
#   ./run.sh bench plain [queries] [services] [namespaces]
#   ./run.sh compare plain jwt enclave [queries] [services] [namespaces]
#   ./run.sh plot plain jwt enclave [label1] [label2] [label3]
#   ./run.sh test plain

SCRIPT_DIR="./scripts"
RESULTS_DIR="./results"

# Defaults
DEFAULT_QUERIES=300
DEFAULT_SERVICES=20
DEFAULT_NAMESPACES=1

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

die() {
    echo -e "${RED}ERROR: $*${NC}" >&2
    exit 1
}

info() {
    echo -e "${GREEN}$*${NC}"
}

warn() {
    echo -e "${YELLOW}$*${NC}"
}

# Generate JWT keys if needed
setup_jwt() {
    if [ ! -f ./approaches/jwt/private.pem ]; then
        info "Generating JWT keys..."
        cd ./approaches/jwt && ../../build/jwt-tools generate-keys --algorithm EdDSA
        cd ../..
    fi
}

# Generate JWT keys and AES key for enclave-jwt if needed
setup_enclave_jwt() {
    if [ ! -f ./approaches/enclave-jwt/private.pem ]; then
        info "Generating JWT keys for enclave-jwt..."
        cd ./approaches/enclave-jwt && ../../build/jwt-tools generate-keys --algorithm EdDSA
        cd ../..
    fi
    # AES key is generated by start.sh
}

# Generate fresh WKD-IBE test key
setup_wkdibe() {
    info "Generating WKD-IBE test key..."
    rm -f ./approaches/wkdibe/test.key
    ./build/etcd-client wkdibe keygen \
        --params ./approaches/wkdibe/params.bin \
        --master-key ./approaches/wkdibe/master.key \
        --domain "testservice.test.svc.cluster.local" \
        --output ./approaches/wkdibe/test.key >/dev/null 2>&1 || die "WKD-IBE key generation failed"
}

# Generate fresh Calypso test keys
#
# Reader keys must be tied to a concrete domain (calypso.go:940 rejects
# wildcards in DecryptAndVerify). So the chain is:
#   1. namespace wildcard writer (*.test.svc.cluster.local)
#   2. derive concrete service writer for testservice.test.svc.cluster.local
#   3. derive reader from the concrete writer
setup_calypso() {
    info "Generating Calypso test keys..."
    rm -f ./approaches/calypso/test-namespace.key \
          ./approaches/calypso/test-service.key \
          ./approaches/calypso/test-namespace-reader.key
    ./build/etcd-client calypso keygen \
        --params ./approaches/calypso/params.bin \
        --authority ./approaches/calypso/authority.bin \
        --domain "*.test.svc.cluster.local" \
        --writer \
        --output ./approaches/calypso/test-namespace.key >/dev/null 2>&1 || die "Calypso namespace writer key generation failed"
    ./build/etcd-client calypso keyder \
        --params ./approaches/calypso/params.bin \
        --parent-key ./approaches/calypso/test-namespace.key \
        --domain "testservice.test.svc.cluster.local" \
        --output ./approaches/calypso/test-service.key >/dev/null 2>&1 || die "Calypso service writer key derivation failed"
    ./build/etcd-client calypso reader \
        --writer ./approaches/calypso/test-service.key \
        --output ./approaches/calypso/test-namespace-reader.key >/dev/null 2>&1 || die "Calypso reader key generation failed"
}

# Benchmark single approach
bench() {
    local approach=$1
    local queries=${2:-$DEFAULT_QUERIES}
    local services=${3:-$DEFAULT_SERVICES}
    local namespaces=${4:-$DEFAULT_NAMESPACES}

    [ -z "$approach" ] && die "Usage: $0 bench <approach> [queries] [services] [namespaces]"

    # Setup if needed
    case "$approach" in
        jwt|jwt-udp) setup_jwt ;;
        enclave-jwt|enclave-jwt-udp) setup_enclave_jwt ;;
    esac

    info "========================================="
    info "Benchmarking: $approach"
    info "Parameters: queries=$queries, services=$services, namespaces=$namespaces"
    info "========================================="

    # Ensure clean state
    $SCRIPT_DIR/stop.sh 2>/dev/null || true

    # Start services
    $SCRIPT_DIR/start.sh "$approach"

    # Wait for services
    info "Waiting for services to be ready..."
    sleep 5

    # Verify etcd is up
    for i in {1..5}; do
        if ./build/etcd-client -clear >/dev/null 2>&1; then
            info "Services ready"
            break
        fi
        [ $i -eq 5 ] && die "Services failed to start"
        warn "Waiting for etcd... (attempt $i/5)"
        sleep 2
    done

    # Populate data
    $SCRIPT_DIR/populate-etcd.sh -a "$approach" -s "$services" -n "$namespaces"

    # Verify data
    info "Verifying etcd data..."
    ./build/etcd-client -list >/dev/null 2>&1 || die "etcd not accessible or no data"

    # Run benchmark
    info "Starting benchmark..."
    $SCRIPT_DIR/benchmark-dns-latency.sh -a "$approach" -s "$services" -n "$namespaces" -x 0 -q "$queries"

    # Cleanup
    $SCRIPT_DIR/stop.sh "$approach"

    info "✓ $approach complete"
}

# Auto-generate label from approach name
auto_label() {
    local approach=$1

    echo "$approach" | sed 's/-udp/-UDP/g; s/-/ /g' | awk '{
        for(i=1;i<=NF;i++) {
            word=$i
            if (word == "plain") word = "Plain"
            else if (word == "jwt") word = "JWT"
            else if (word == "enclave") word = "Enclave"
            else if (word == "wkdibe") word = "WKD-IBE"
            else if (word == "calypso") word = "Calypso"
            else word = toupper(substr(word,1,1)) substr(word,2)
            printf "%s ", word
        }
    }' | sed 's/ $//'
}

# Plot comparison from existing results
plot() {
    [ $# -lt 1 ] && die "Usage: $0 plot <approach1> <approach2> [approach3...] [label1] [label2] [label3...]"

    local approaches=()
    local labels=()
    local parsing_labels=false

    # Separate approaches from labels
    # If number of args > number of valid approach files, assume rest are labels
    for arg in "$@"; do
        if ! $parsing_labels && ls -t "$RESULTS_DIR/$arg"-[0-9]*-latencies.csv 2>/dev/null | head -1 | grep -q .; then
            approaches+=("$arg")
        else
            parsing_labels=true
            labels+=("$arg")
        fi
    done

    # Auto-generate labels if not provided
    if [ ${#labels[@]} -eq 0 ]; then
        for approach in "${approaches[@]}"; do
            labels+=("$(auto_label "$approach")")
        done
    fi

    # Validate we have enough labels
    [ ${#labels[@]} -ne ${#approaches[@]} ] && die "Mismatch: ${#approaches[@]} approaches but ${#labels[@]} labels"

    info "========================================="
    info "Generating comparison plot"
    info "Approaches: ${approaches[*]}"
    info "Labels: ${labels[*]}"
    info "========================================="

    local timestamp=$(date +%Y%m%d-%H%M%S)
    local output_name=$(echo "${approaches[@]}" | tr ' ' '-')
    local plot_file="$RESULTS_DIR/${output_name}-comparison-${timestamp}.png"
    local plot_args="$plot_file"

    # Convert CSV to DAT and build args
    for i in "${!approaches[@]}"; do
        local approach="${approaches[$i]}"
        local label="${labels[$i]}"

        local csv=$(ls -t "$RESULTS_DIR/$approach"-[0-9]*-latencies.csv 2>/dev/null | head -1)
        [ -z "$csv" ] && die "No results found for $approach"

        local dat="${csv%.csv}.dat"
        $SCRIPT_DIR/plot/csv-to-dat.sh "$csv" "$dat"

        plot_args="$plot_args \"$dat\" \"$label\""
    done

    # Generate plot
    local config_file="$RESULTS_DIR/.plot-config-${timestamp}.txt"
    echo "$plot_args" > "$config_file"

    gnuplot -c $SCRIPT_DIR/plot/plot-cdf-compare.gpi "$config_file"
    rm -f "$config_file"

    info "✓ Plot saved: $plot_file"
}

# Compare multiple approaches (bench + plot)
compare() {
    [ $# -lt 2 ] && die "Usage: $0 compare <approach1> <approach2> [approach3...] [queries] [services] [namespaces]"

    local approaches=()
    local queries=$DEFAULT_QUERIES
    local services=$DEFAULT_SERVICES
    local namespaces=$DEFAULT_NAMESPACES

    # Parse approaches and optional numeric parameters
    for arg in "$@"; do
        if [[ "$arg" =~ ^[0-9]+$ ]] && [ ${#approaches[@]} -ge 2 ]; then
            # First number after approaches is queries, second is services, third is namespaces
            if [ $queries -eq $DEFAULT_QUERIES ]; then
                queries=$arg
            elif [ $services -eq $DEFAULT_SERVICES ]; then
                services=$arg
            elif [ $namespaces -eq $DEFAULT_NAMESPACES ]; then
                namespaces=$arg
            fi
        else
            approaches+=("$arg")
        fi
    done

    [ ${#approaches[@]} -lt 2 ] && die "Need at least 2 approaches to compare"

    info "========================================="
    info "Comparing: ${approaches[*]}"
    info "Parameters: queries=$queries, services=$services, namespaces=$namespaces"
    info "========================================="

    # Benchmark each approach
    for approach in "${approaches[@]}"; do
        bench "$approach" "$queries" "$services" "$namespaces"
    done

    # Generate plot
    plot "${approaches[@]}"
}

# Run smoke test
test() {
    local approach=${1:-plain}

    # Validate approach
    case "$approach" in
        plain|jwt|enclave|enclave-jwt|wkdibe|calypso)
            ;;
        *)
            die "Invalid approach: $approach\nValid approaches: plain, jwt, enclave, enclave-jwt, wkdibe, calypso"
            ;;
    esac

    info "========================================="
    info "Testing: $approach"
    info "========================================="

    case "$approach" in
        jwt) setup_jwt ;;
        enclave-jwt) setup_enclave_jwt ;;
        wkdibe) setup_wkdibe ;;
        calypso) setup_calypso ;;
    esac

    # Ensure clean state
    $SCRIPT_DIR/stop.sh 2>/dev/null || true

    # Start services
    info "Starting services..."
    $SCRIPT_DIR/start.sh "$approach" >/dev/null 2>&1
    sleep 5

    # Insert test record
    info "Inserting test record..."
    case "$approach" in
        plain)
            ./build/etcd-client -register testservice.test.svc.cluster.local=10.0.1.100 || die "Insert failed"
            ;;
        enclave)
            CRYPTO_TYPE=aes AES_KEY_FILE=./approaches/enclave/aes.key \
                ./build/etcd-client -register testservice.test.svc.cluster.local=10.0.1.100 || die "Insert failed"
            ;;
        jwt)
            ./build/etcd-client -register testservice.test.svc.cluster.local=10.0.1.100 || die "Insert failed"
            ;;
        enclave-jwt)
            CRYPTO_TYPE=aes AES_KEY_FILE=./approaches/enclave-jwt/aes.key \
                ./build/etcd-client -register testservice.test.svc.cluster.local=10.0.1.100 || die "Insert failed"
            ;;
        wkdibe)
            CRYPTO_TYPE=wkdibe \
                WKDIBE_PARAMS_FILE=./approaches/wkdibe/params.bin \
                WKDIBE_KEY_FILE=./approaches/wkdibe/test.key \
                WKDIBE_MAX_DEPTH=6 \
                ./build/etcd-client -register testservice.test.svc.cluster.local=10.0.1.100 || die "Insert failed"
            ;;
        calypso)
            CRYPTO_TYPE=calypso \
                CALYPSO_PARAMS_FILE=./approaches/calypso/params.bin \
                CALYPSO_WRITER_KEY=./approaches/calypso/test-namespace.key \
                CALYPSO_MAX_DEPTH=7 \
                ./build/etcd-client -register testservice.test.svc.cluster.local=10.0.1.100 || die "Insert failed"
            ;;
    esac

    # Query
    info "Querying..."
    case "$approach" in
        jwt)
            local token=$(cd ./approaches/jwt && ../../build/jwt-tools generate-token \
                --algorithm EdDSA --client-id "test-client" --permissions "query" --expiry "1000h" | tail -1)
            result=$(./build/q -i --jwt --token "$token" A testservice.test.svc.cluster.local @https://localhost:8443 2>&1)
            ;;
        enclave-jwt)
            local token=$(cd ./approaches/enclave-jwt && ../../build/jwt-tools generate-token \
                --algorithm EdDSA --client-id "test-client" --permissions "query" --expiry "1000h" | tail -1)
            result=$(./build/q -i --jwt --token "$token" A testservice.test.svc.cluster.local @https://localhost:8443 2>&1)
            ;;
        wkdibe)
            result=$(./build/q -i A testservice.test.svc.cluster.local @https://localhost:8443 \
                --wkdibe --params=./approaches/wkdibe/params.bin --key=./approaches/wkdibe/test.key 2>&1)
            ;;
        calypso)
            result=$(./build/q -i A testservice.test.svc.cluster.local @https://localhost:8443 \
                --calypso --params=./approaches/calypso/params.bin --key=./approaches/calypso/test-namespace-reader.key 2>&1)
            ;;
        *)
            result=$(./build/q -i A testservice.test.svc.cluster.local @https://localhost:8443 2>&1)
            ;;
    esac

    echo "$result" | grep -q "10.0.1.100" || die "Query failed or wrong result:\n$result"

    # Cleanup
    $SCRIPT_DIR/stop.sh >/dev/null 2>&1

    info "✓ $approach test passed"
}

# Clean results and temporary files
clean() {
    info "Cleaning results directory..."
    rm -rf "$RESULTS_DIR"/*
    info "Cleaning JWT tokens and keys..."
    rm -f ./approaches/jwt/private.pem ./approaches/jwt/public.pem
    rm -f ./approaches/enclave-jwt/private.pem ./approaches/enclave-jwt/public.pem
    info "✓ Clean complete (certificates and crypto parameters preserved)"
}

# Stop all services
stop() {
    info "Stopping all services..."
    $SCRIPT_DIR/stop.sh 2>/dev/null || true
    info "✓ Services stopped"
}

# List recent results
list() {
    if ls "$RESULTS_DIR"/*.csv >/dev/null 2>&1; then
        local total=$(ls "$RESULTS_DIR"/*.csv 2>/dev/null | wc -l | tr -d ' ')
        info "Recent benchmark results ($total files):"
        ls -t "$RESULTS_DIR"/*.csv 2>/dev/null | head -20 | while read -r file; do
            local entries=$(wc -l < "$file" | tr -d ' ')
            local size=$(ls -lh "$file" | awk '{print $5}')
            local date=$(ls -lh "$file" | awk '{print $6, $7, $8}')
            printf "%s [%s entries, %s] (%s)\n" "$(basename "$file")" "$entries" "$size" "$date"
        done
        [ "$total" -gt 20 ] && warn "Showing 20 of $total files"
    else
        warn "No results found in $RESULTS_DIR"
    fi
}

# Helper function to run full benchmark with specified base approaches
_run_full_benchmark() {
    local title=$1
    shift
    local base_approaches=("$@")

    local all_approaches=()

    # Add both DoH and UDP variants
    for approach in "${base_approaches[@]}"; do
        all_approaches+=("$approach")
        all_approaches+=("${approach}-udp")
    done

    info "Will benchmark ${#all_approaches[@]} approach variants:"
    for approach in "${all_approaches[@]}"; do
        info "  - $approach"
    done
    echo ""

    # Benchmark each approach
    local completed=0
    local total=${#all_approaches[@]}
    for approach in "${all_approaches[@]}"; do
        completed=$((completed + 1))
        info "[$completed/$total] Benchmarking $approach..."
        bench "$approach" "$queries" "$services" "$namespaces"
        echo ""
    done

    info "========================================="
    info "$title complete!"
    info "Generating combined comparison plot..."
    info "========================================="

    # Generate combined plot with all approaches
    plot "${all_approaches[@]}"

    info "✓ All benchmarks and plot complete"
}

# Full benchmark - core approaches (excludes enclave variants)
full() {
    local queries=${1:-$DEFAULT_QUERIES}
    local services=${2:-$DEFAULT_SERVICES}
    local namespaces=${3:-$DEFAULT_NAMESPACES}

    info "========================================="
    info "Full Benchmark - Core Approaches"
    info "Parameters: queries=$queries, services=$services, namespaces=$namespaces"
    info "========================================="

    local base_approaches=("plain" "jwt" "wkdibe" "calypso")
    _run_full_benchmark "Full benchmark" "${base_approaches[@]}"
}

# Full benchmark with enclave approaches
full-w-ego() {
    local queries=${1:-$DEFAULT_QUERIES}
    local services=${2:-$DEFAULT_SERVICES}
    local namespaces=${3:-$DEFAULT_NAMESPACES}

    info "========================================="
    info "Full Benchmark - All Approaches (with Enclave)"
    info "Parameters: queries=$queries, services=$services, namespaces=$namespaces"
    info "========================================="

    local base_approaches=("plain" "jwt" "enclave" "enclave-jwt" "wkdibe" "calypso")
    _run_full_benchmark "Full benchmark with enclave" "${base_approaches[@]}"
}

# Main
main() {
    local cmd=$1
    shift

    case "$cmd" in
        bench|benchmark)
            bench "$@"
            ;;
        compare|cmp)
            compare "$@"
            ;;
        plot)
            plot "$@"
            ;;
        full)
            full "$@"
            ;;
        full-w-ego)
            full-w-ego "$@"
            ;;
        test)
            test "$@"
            ;;
        clean)
            clean
            ;;
        stop)
            stop
            ;;
        list|ls)
            list
            ;;
        help|-h|--help|"")
            cat <<EOF
Calypso DNS Evaluation Runner

USAGE:
    $0 <command> [arguments]

COMMANDS:
    bench <approach> [queries] [services] [namespaces]
        Benchmark single approach
        Example: $0 bench plain 1000 50 5

    compare <approach1> <approach2> [...] [queries] [services] [namespaces]
        Benchmark multiple approaches and generate comparison plot
        Example: $0 compare plain jwt enclave 1000 50 5

    plot <approach1> <approach2> [...] [label1] [label2] [...]
        Generate plot from existing results (no re-run)
        Example: $0 plot plain jwt calypso
        Example: $0 plot calypso calypso-udp "Calypso DoH" "Calypso UDP"

    full [queries] [services] [namespaces]
        Benchmark core approaches (DoH + UDP) and generate comparison plot
        Runs: plain, jwt, wkdibe, calypso (excludes enclave variants)
        Example: $0 full 500 30 5

    full-w-ego [queries] [services] [namespaces]
        Benchmark all approaches including enclave (DoH + UDP)
        Runs: plain, jwt, enclave, enclave-jwt, wkdibe, calypso (both variants)
        Example: $0 full-w-ego 500 30 5

    test [approach]
        Run smoke test for approach (default: plain)
        Example: $0 test jwt

    clean
